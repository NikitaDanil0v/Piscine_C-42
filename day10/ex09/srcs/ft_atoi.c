int		ft_atoi(char *str)
{
	int		r;
	int		minus;

	r = 0;
	minus = 0;
	while (*str == ' ' || (*str > 8 && *str < 14))
		str++;
	if (*str == '+')
		str++;
	else if (*str == '-')
	{
		minus = 1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		r = r * 10 + *str - '0';
		str++;
	}
	if (minus)
		r = -r;
	return (r);
}
